function displayHelp(id) {
    var elem = document.getElementById(id);
    if (elem.style.display == 'block') {
        elem.style.display = 'none';
    } else {
        elem.style.display = 'block';
    }
}
function closeHelp(id) {
    var elem = document.getElementById(id);
    if (elem.style.display == 'block') {
        elem.style.display = 'none';
    }
}
